//unit = 0.04 * windowWidth; for reference, already defined in main

function createMap() {
  for (let row = 0; row < map.length; row++) {
    for (let col = 0; col < map[row].length; col++) {
      if (map[row][col] === 1) {
        blocks.push(new Sprite(col * unit, unit * row, unit, unit, blockImg));
      }
      else if (map[row][col] === 6) {
        zombies.push(new Zombie(col * unit, unit * row, unit, 2 * unit, 0, 0, zombieRightImg));
      }
      else if (map[row][col] === 7) {
        coins.push(new Sprite(col * unit, unit * row, unit, unit, coinImg));
      }
      else if (map[row][col] === 8) {
        player = new Player(col * unit, unit * row, 1 * unit, 2 * unit, 0.15 * unit, johnImg);
      }
      else if (map[row][col] === 9) {
        //map[row].splice(col,1);
      }
    }
  }
}